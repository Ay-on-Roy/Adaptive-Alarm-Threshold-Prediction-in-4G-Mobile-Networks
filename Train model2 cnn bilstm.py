# EVALUATE ON TEST SET
# ============================================================

def evaluate(model, X_df, y_df):
    ds = AlarmDataset(X_df, y_df)
    dl = DataLoader(ds, batch_size=BATCH_SIZE * 2, shuffle=False,
                    num_workers=2, pin_memory=True)

    model.eval()
    preds = []
    with torch.no_grad():
        for seq, ctx, y in dl:
            seq, ctx = seq.to(DEVICE), ctx.to(DEVICE)
            preds.append(model(seq, ctx).cpu().numpy())

    y_pred = np.vstack(preds)
    y_true = y_df[TARGET_COLS].values.astype(np.float32)

    for i, (lo, hi) in enumerate(CLIP_RANGES):
        y_pred[:, i] = np.clip(y_pred[:, i], lo, hi)

    per_target = {}
    all_mae, all_rmse, all_r2 = [], [], []

    for i, col in enumerate(TARGET_COLS):
        mae  = mean_absolute_error(y_true[:, i], y_pred[:, i])
        rmse = np.sqrt(mean_squared_error(y_true[:, i], y_pred[:, i]))
        r2   = r2_score(y_true[:, i], y_pred[:, i])
        per_target[col] = {'MAE': round(mae, 4),
                           'RMSE': round(rmse, 4),
                           'R2': round(r2, 4)}
        all_mae.append(mae)
        all_rmse.append(rmse)
        all_r2.append(r2)

    return y_pred, per_target, np.mean(all_mae), np.mean(all_rmse), np.mean(all_r2)


