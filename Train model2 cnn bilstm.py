class AlarmDataset(Dataset):
    def __init__(self, X_df, y_df):
        inactive = X_df[hourly_inactive_cols].values.astype(np.float32)
        fluct    = X_df[hourly_fluct_cols].values.astype(np.float32)
        self.seq = torch.tensor(np.stack([inactive, fluct], axis=2))  # (N,24,2)
        self.ctx = torch.tensor(X_df[context_cols].values.astype(np.float32))
        self.y   = torch.tensor(y_df[TARGET_COLS].values.astype(np.float32))

    def __len__(self):
        return len(self.y)

    def __getitem__(self, idx):
        return self.seq[idx], self.ctx[idx], self.y[idx]


def make_loaders(X_df, y_df, X_val_df=None, y_val_df=None, batch_size=BATCH_SIZE):
    train_ds = AlarmDataset(X_df, y_df)
    train_dl = DataLoader(train_ds, batch_size=batch_size,
                          shuffle=True, num_workers=2, pin_memory=True)
    if X_val_df is not None:
        val_ds = AlarmDataset(X_val_df, y_val_df)
        val_dl = DataLoader(val_ds, batch_size=batch_size * 2,
                            shuffle=False, num_workers=2, pin_memory=True)
        return train_dl, val_dl
    return train_dl

