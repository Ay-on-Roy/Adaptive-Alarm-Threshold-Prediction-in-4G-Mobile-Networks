class CNNBiLSTMAttention(nn.Module):
    def __init__(self, n_ctx, cnn_channels=64, lstm_hidden=128,
                 lstm_layers=2, attn_heads=4, dropout=0.2, output_dim=4):
        super().__init__()

        self.lstm_hidden = lstm_hidden

        # CNN Encoder
        self.cnn = nn.Sequential(
            nn.Conv1d(2, cnn_channels, kernel_size=3, padding=1),
            nn.BatchNorm1d(cnn_channels),
            nn.ReLU(),
            nn.Conv1d(cnn_channels, cnn_channels * 2, kernel_size=3, padding=1),
            nn.BatchNorm1d(cnn_channels * 2),
            nn.ReLU(),
            nn.Dropout(dropout),
        )
        cnn_out = cnn_channels * 2

        # BiLSTM
        self.bilstm = nn.LSTM(
            input_size=cnn_out,
            hidden_size=lstm_hidden,
            num_layers=lstm_layers,
            batch_first=True,
            bidirectional=True,
            dropout=dropout if lstm_layers > 1 else 0.0,
        )
        lstm_out = lstm_hidden * 2  # bidirectional

        # Make lstm_out divisible by attn_heads
        # Pad if necessary
        if lstm_out % attn_heads != 0:
            self.attn_proj = nn.Linear(lstm_out, attn_heads * (lstm_out // attn_heads + 1))
            attn_dim = attn_heads * (lstm_out // attn_heads + 1)
        else:
            self.attn_proj = None
            attn_dim = lstm_out

        # Multi-Head Attention
        self.attention = nn.MultiheadAttention(
            embed_dim=attn_dim,
            num_heads=attn_heads,
            dropout=dropout,
            batch_first=True,
        )
        self.attn_norm  = nn.LayerNorm(attn_dim)
        self.query_proj = nn.Linear(n_ctx, attn_dim)

        # Context MLP
        self.ctx_mlp = nn.Sequential(
            nn.Linear(n_ctx, 256),
            nn.LayerNorm(256),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(256, 128),
            nn.LayerNorm(128),
            nn.ReLU(),
            nn.Dropout(dropout),
        )

        # Fusion + Output
        fused_dim = attn_dim + 128
        self.fusion = nn.Sequential(
            nn.Linear(fused_dim, 256),
            nn.LayerNorm(256),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(256, 128),
            nn.ReLU(),
        )
        self.output_head = nn.Linear(128, output_dim)

    def forward(self, seq, ctx):
        # seq: (B, 24, 2)  ctx: (B, n_ctx)
        x = seq.permute(0, 2, 1)            # (B, 2, 24)
        x = self.cnn(x)                      # (B, cnn_out, 24)
        x = x.permute(0, 2, 1)              # (B, 24, cnn_out)

        lstm_out, _ = self.bilstm(x)         # (B, 24, lstm_out)

        if self.attn_proj is not None:
            lstm_out = self.attn_proj(lstm_out)

        query = self.query_proj(ctx).unsqueeze(1)   # (B, 1, attn_dim)
        attn_out, _ = self.attention(query, lstm_out, lstm_out)
        attn_out = attn_out.squeeze(1)               # (B, attn_dim)
        attn_out = self.attn_norm(attn_out + query.squeeze(1))

        ctx_out = self.ctx_mlp(ctx)                  # (B, 128)

        fused = torch.cat([attn_out, ctx_out], dim=1)
        fused = self.fusion(fused)
        out   = self.output_head(fused)              # (B, 4)
        return out


# ============================================================
# TRAIN ONE EPOCH
# ============================================================

def train_one_epoch(model, loader, optimizer, criterion):
    model.train()
    total_loss = 0.0
    for seq, ctx, y in loader:
        seq, ctx, y = seq.to(DEVICE), ctx.to(DEVICE), y.to(DEVICE)
        optimizer.zero_grad()
        pred = model(seq, ctx)
        loss = criterion(pred, y)
        loss.backward()
        nn.utils.clip_grad_norm_(model.parameters(), GRAD_CLIP)
        optimizer.step()
        total_loss += loss.item()
    return total_loss / len(loader)


# ============================================================
# VALIDATE
# ============================================================

def validate(model, loader, criterion):
    model.eval()
    total_loss = 0.0
    with torch.no_grad():
        for seq, ctx, y in loader:
            seq, ctx, y = seq.to(DEVICE), ctx.to(DEVICE), y.to(DEVICE)
            pred = model(seq, ctx)
            total_loss += criterion(pred, y).item()
    return total_loss / len(loader)


# ============================================================
# FULL TRAINING WITH EARLY STOPPING
# ============================================================

def train_model(config, train_loader, val_loader,
                max_epochs=MAX_EPOCHS, patience=PATIENCE_SEARCH,
                verbose=False):

    model = CNNBiLSTMAttention(
        n_ctx        = len(context_cols),
        cnn_channels = config['cnn_channels'],
        lstm_hidden  = config['lstm_hidden'],
        lstm_layers  = config['lstm_layers'],
        attn_heads   = config['attn_heads'],
        dropout      = config['dropout'],
        output_dim   = 4,
    ).to(DEVICE)

    criterion = nn.HuberLoss(delta=1.0)
    optimizer = optim.AdamW(
        model.parameters(),
        lr           = config['lr'],
        weight_decay = config['weight_decay'],
    )
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=max_epochs)

    best_val_loss  = float('inf')
    patience_count = 0
    best_state     = None
    train_losses   = []
    val_losses     = []

    for epoch in range(1, max_epochs + 1):
        tr_loss  = train_one_epoch(model, train_loader, optimizer, criterion)
        val_loss = validate(model, val_loader, criterion)
        scheduler.step()

        train_losses.append(tr_loss)
        val_losses.append(val_loss)

        if verbose and (epoch % 5 == 0 or epoch == 1):
            print(f"    Epoch {epoch:3d}  train={tr_loss:.4f}  val={val_loss:.4f}")

        if val_loss < best_val_loss:
            best_val_loss  = val_loss
            patience_count = 0
            best_state     = copy.deepcopy(model.state_dict())
        else:
            patience_count += 1
            if patience_count >= patience:
                if verbose:
                    print(f"    Early stop at epoch {epoch}")
                break

    model.load_state_dict(best_state)
    return model, best_val_loss, train_losses, val_losses


