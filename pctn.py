# ── PCTN-v2 Architecture ─────────────────────────────────────

class AttentionPool(nn.Module):
    """Improvement 1: weighted pooling over hourly tokens instead of avg."""
    def __init__(self, d):
        super().__init__()
        self.w = nn.Parameter(torch.randn(d))
    def forward(self, H):          # H: (B,N,d)
        w = F.softmax(H @ self.w, dim=1)   # (B,N)
        return (w.unsqueeze(-1) * H).sum(1) # (B,d)

class DynamicAlpha(nn.Module):
    """Improvement 3: alpha depends on (mu, sigma, context) not a fixed scalar."""
    def __init__(self, d_in, n=4):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(d_in, 32), nn.ReLU(), nn.Linear(32, n))
        nn.init.constant_(self.net[-1].bias, 0.405)  # softplus(0.405)~0.75
    def forward(self, x):
        return F.softplus(self.net(x)) + 0.1

class PCTN_v2(nn.Module):
    def __init__(self, n_h, n_c, d=D_MODEL):
        super().__init__()
        # Stage 1: Context encoder
        self.ctx_enc = nn.Sequential(
            nn.Linear(n_c, 128), nn.LayerNorm(128), nn.GELU(), nn.Dropout(DROPOUT),
            nn.Linear(128, d),   nn.LayerNorm(d),   nn.GELU())

        # Stage 2: Pattern encoder + attention pool (Improvement 1)
        self.h_embed   = nn.Linear(1, d)
        enc_layer      = nn.TransformerEncoderLayer(
            d_model=d, nhead=N_HEADS, dim_feedforward=128,
            dropout=DROPOUT, batch_first=True, norm_first=True)
        self.pat_enc   = nn.TransformerEncoder(enc_layer, num_layers=N_LAYERS)
        self.attn_pool = AttentionPool(d)

        # Stage 3a: Forward cross-attention ctx -> pattern (original)
        self.ca_fwd = nn.MultiheadAttention(d, N_HEADS, dropout=DROPOUT,
                                             batch_first=True, kdim=d, vdim=d)
        self.ln_fwd = nn.LayerNorm(d)

        # Stage 3b: Backward cross-attention pattern -> ctx (Improvement 2)
        self.ca_bwd = nn.MultiheadAttention(d, N_HEADS, dropout=DROPOUT,
                                             batch_first=True, kdim=d, vdim=d)
        self.ln_bwd = nn.LayerNorm(d)

        d2 = d * 2  # 128 fused dim

        # Stage 4: Distribution head
        self.fuse     = nn.Sequential(nn.Linear(d2, d2), nn.ReLU(), nn.Dropout(DROPOUT))
        self.mu_head  = nn.Linear(d2, 4)
        self.sig_head = nn.Linear(d2, 4)

        # Stage 5 — Mixed outputs
        # Improvement 4a: Categorical head for threshold_hours (discrete 2..8 = 7 classes)
        self.cat_head = nn.Linear(d2, 7)

        # Improvement 4b: Bernoulli gates for floor-dominated fluctuation targets
        self.gate_f  = nn.Linear(d2, 1)
        self.gate_hf = nn.Linear(d2, 1)

        # Improvement 3: Dynamic alpha
        self.dyn_alpha = DynamicAlpha(d2 + 8, n=4)  # input: fused + mu + sigma

    def encode(self, xh, xc):
        """Shared encoding used in both forward and loss computation."""
        v_ctx = self.ctx_enc(xc)                             # (B,d)
        H     = self.pat_enc(self.h_embed(xh.unsqueeze(-1))) # (B,48,d)
        v_pat = self.attn_pool(H)                            # (B,d)

        # Forward CA: ctx queries pattern
        att_f, _ = self.ca_fwd(v_ctx.unsqueeze(1), H, H)
        v_f = self.ln_fwd(att_f.squeeze(1) + v_ctx)         # (B,d)

        # Backward CA: pattern queries ctx
        att_b, _ = self.ca_bwd(v_pat.unsqueeze(1),
                                v_ctx.unsqueeze(1), v_ctx.unsqueeze(1))
        v_b = self.ln_bwd(att_b.squeeze(1) + v_pat)         # (B,d)

        v_fused = self.fuse(torch.cat([v_f, v_b], -1))      # (B,d2)
        return v_fused

    def forward(self, xh, xc):
        v = self.encode(xh, xc)                              # (B,d2)

        mu  = self.mu_head(v)                                # (B,4)
        sig = F.softplus(self.sig_head(v)) + 0.1             # (B,4)

        # Dynamic alpha (Improvement 3)
        alpha = self.dyn_alpha(torch.cat([v, mu, sig], -1)) # (B,4)
        theta = mu + alpha * sig                             # (B,4)

        # Categorical head for threshold_hours (Improvement 4a)
        cat_lg  = self.cat_head(v)                           # (B,7)
        classes = torch.arange(2, 9, dtype=torch.float32, device=v.device)
        h_pred  = (F.softmax(cat_lg, -1) * classes).sum(-1, keepdim=True)  # (B,1)

        # Bernoulli gates for fluctuation (Improvement 4b)
        gf  = torch.sigmoid(self.gate_f(v))   # (B,1) P(above floor)
        ghf = torch.sigmoid(self.gate_hf(v))  # (B,1)
        f_pred  = gf  * theta[:, 2:3] + 1.0  # floor is 1.0
        hf_pred = ghf * theta[:, 3:4] + 1.0

        out = torch.cat([h_pred, theta[:, 1:2], f_pred, hf_pred], -1)  # (B,4)
        return out, cat_lg, gf, ghf, mu, sig, alpha

# ── Loss ─────────────────────────────────────────────────────

def pinball(pred, target, tau):
    e = target - pred
    return torch.where(e >= 0, tau * e, (tau-1) * e).mean()

bce_fn = nn.BCELoss()
ce_fn  = nn.CrossEntropyLoss()

def loss_fn(out, cat_lg, gf, ghf, mu, sig, alpha, y):
    # threshold_hours: CrossEntropy on 7 classes
    l_h = ce_fn(cat_lg, (y[:,0].long()-2).clamp(0,6))

    # threshold_inactive_min: Pinball tau=0.75
    l_i = pinball(out[:,1], y[:,1], 0.75)

    # fluctuation: Pinball + BCE gate
    l_fg = bce_fn(gf.squeeze(-1),  (y[:,2]>1).float())
    l_fp = pinball(out[:,2], y[:,2], 0.90)

    l_hfg = bce_fn(ghf.squeeze(-1), (y[:,3]>1).float())
    l_hfp = pinball(out[:,3], y[:,3], 0.90)

    return (l_h + l_i + l_fg + l_fp + l_hfg + l_hfp) / 5.0

